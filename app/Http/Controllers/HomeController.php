    public function UpdateExchangeRate(){

        $response =$this->getExchangeRate();

        if($response->status()==200){
            if(ExchangeRate::count() > 0){
                foreach($response->collect()['conversion_rates'] as $key=>$rate){
                    ExchangeRate::where('code',$key)->update(['exchange_rate'=>$rate]);
                }

            }else{
                $rate_list=array();
                foreach($response->collect()['conversion_rates'] as $key=>$rate){
                    $list=array();
                    $list['base_currency_code']=config('app.base_currency');
                    $list['exchange_rate']=$rate;
                    $list['code']=$key;
                    $list['created_at']=now();
                    $rate_list[]=$list;
                }
                ExchangeRate::insert($rate_list);

            }

        }

    }
    public function getExchangeRate(){
        $url='https://v6.exchangerate-api.com/v6/';
        $url.=config('app.exchange_rate_api_key');
        $url.='/latest/';
        $url.=config('app.base_currency');

        return  Http::get($url);
    }


